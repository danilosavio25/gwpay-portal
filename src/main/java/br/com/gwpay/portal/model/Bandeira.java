package br.com.gwpay.portal.model;

public class Bandeira {
	private int id;
	private String nome;
	
	public Bandeira() {
	}
	
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNome() {
		return nome;
	}
	public void setNome(String nome) {
		this.nome = nome;
	}
	
	
}
